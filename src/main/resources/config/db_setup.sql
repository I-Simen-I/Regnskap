##### DROP #####
DROP TABLE IF EXISTS UTGIFT;
DROP TABLE IF EXISTS CATEGORY;

##### CREATE #####
#KODEVERK
CREATE TABLE CATEGORY (
  CATEGORY_ID INT(11)      NOT NULL AUTO_INCREMENT,
  NAME        VARCHAR(55)  NOT NULL UNIQUE,
  CREATED     TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CREATED_BY  VARCHAR(255) NOT NULL,
  PRIMARY KEY (CATEGORY_ID)
) ENGINE = INNODB DEFAULT CHARSET = UTF8;

#DOMAIN
CREATE TABLE UTGIFT (
  UTGIFT_ID    INT(11)        NOT NULL AUTO_INCREMENT,
  MAANED       VARCHAR(55)    NOT NULL,
  KATEGORI_ID  INT(11)        NOT NULL,
  BELOP        DECIMAL(10, 0) NOT NULL,
  BESKRIVELSE  VARCHAR(1000),
  OPPRETTET    TIMESTAMP      NOT NULL DEFAULT CURRENT_TIMESTAMP,
  OPPRETTET_AV VARCHAR(255)   NOT NULL,
  PRIMARY KEY (UTGIFT_ID),
  FOREIGN KEY (KATEGORI_ID) REFERENCES CATEGORY (CATEGORY_ID)
) ENGINE = INNODB DEFAULT CHARSET = UTF8;

